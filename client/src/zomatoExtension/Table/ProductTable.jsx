export const statusOptions = [
  { name: "Veg", uid: "veg" },
  { name: "Non-Veg", uid: "non_veg" },
  { name: "Egg", uid: "egg" },
];

const getStatus = (status) => {
  let foundStatus;
  statusOptions.map((allStatus, index) => {
    if (allStatus.uid == status) {
      foundStatus = allStatus.name;
    }
  });
  return foundStatus;
};

export function capitalize(s) {
  return s ? s.charAt(0).toUpperCase() + s.slice(1).toLowerCase() : "";
}

